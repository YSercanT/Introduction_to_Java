public class Primitive {
   public static void main(String[] args) {
    /*
     * Java programming language has eight primitive data types :
     * Those are :aaa
     * int, short, long, float, double, boolean,char,byte
     * 
     * 
     */
      

    // byte type is between -128 and 127
    byte x=100;//1 byte
    System.out.println("The byte data is "+x);
   // short type is stores whole numbers from -32,768 to 32,767
    short y=1000;//2 bytes
    System.out.println("The short data is "+y);
 
    // int type is stores whole numbers from -2,147,483,648 to 2,147,483,647
    int z=10000;//4 bytes
    System.out.println("The int data is "+z);
    
    // long type is stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
    long w=400000;//8 bytes
    System.out.println("The long data is "+w);

    // float type is stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
    float v=500.1f;//4 bytes
    System.out.println("The float data is "+v);
    
    // Double type is stores fractional numbers. Sufficient for storing 15 decimal digits
    double u=600.923123;//8 bytes
    System.out.println("The double data is "+u);
    
    // boolean type is true or false
    boolean t=true;//1 bit
    System.out.println("The boolean data is "+t);


    //Stores a single character or ASCII values
    char x1='a';//2 bytes
    System.out.println("The char data is "+x1);


   } 
}
